    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DefaultTableModel modelo = (DefaultTableModel) Tabla1.getModel();
            int N = modelo.getRowCount();
            int bajoPeso = 0, normal = 0, sobrePeso = 0, obesidad = 0;

            for (int fila = 0; fila < N; fila++) {
                double altura = Double.parseDouble(Tabla1.getValueAt(fila, 3).toString());
                double peso = Double.parseDouble(Tabla1.getValueAt(fila, 2).toString());
                double imc = peso / Math.pow(altura, 2);

                if (imc < 18.5) {
                    bajoPeso++;
                    modelo.setValueAt("Peso Bajo", fila, 5);
                } else if (imc >= 18.5 && imc < 25) {
                    normal++;
                    modelo.setValueAt("Peso normal", fila, 5);
                } else if (imc >= 25 && imc < 30) {
                    sobrePeso++;
                    modelo.setValueAt("Sobre Peso", fila, 5);
                } else {
                    obesidad++;
                    modelo.setValueAt("Obesidad", fila, 5);
                }

                // GRUPO #3
                modelo.setValueAt(String.format("%.2f", imc), fila, 4);
            }

            Tabla2.setValueAt("" + bajoPeso, 0, 1);
            double pbajo = ((double) bajoPeso / N) * 100;
            Tabla2.setValueAt(String.format("%.2f%%", pbajo), 0, 2);

            Tabla2.setValueAt("" + normal, 1, 1);
            double pnormal = ((double) normal / N) * 100;
            Tabla2.setValueAt(String.format("%.2f%%", pnormal), 1, 2);

            Tabla2.setValueAt("" + sobrePeso, 2, 1);
            double psobre = ((double) sobrePeso / N) * 100;
            Tabla2.setValueAt(String.format("%.2f%%", psobre), 2, 2);

            Tabla2.setValueAt("" + obesidad, 3, 1);
            double pobesidad = ((double) obesidad / N) * 100;
            Tabla2.setValueAt(String.format("%.2f%%", pobesidad), 3, 2);

            int total = bajoPeso + normal + sobrePeso + obesidad;
            Tabla2.setValueAt("" + total, 4, 1);
            Tabla2.setValueAt("100.00%", 4, 2);

        } catch (Exception ex) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

